import torch
import torch.nn as nn
import torch.nn.functional as F

class ConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, 3, padding=1),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),

            nn.Conv2d(out_ch, out_ch, 3, padding=1),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),
        )
    def forward(self, x):
        return self.block(x)

class SimpleBFM(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.conv = nn.Conv2d(in_ch, out_ch, 3, padding=1)
        self.bn   = nn.BatchNorm2d(out_ch)
        self.relu = nn.ReLU(inplace=True)
    def forward(self, x):
        return self.relu(self.bn(self.conv(x)))

class SimpleELPPM(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.p1 = nn.AdaptiveAvgPool2d(1)
        self.p2 = nn.AdaptiveAvgPool2d(2)
        self.p3 = nn.AdaptiveAvgPool2d(4)
        self.c1 = nn.Conv2d(in_ch, out_ch, 1)
        self.c2 = nn.Conv2d(in_ch, out_ch, 1)
        self.c3 = nn.Conv2d(in_ch, out_ch, 1)
        self.fuse = nn.Conv2d(out_ch * 3, out_ch, 1)

    def forward(self, x):
        h, w = x.shape[2:]
        f1 = F.interpolate(self.c1(self.p1(x)), (h, w))
        f2 = F.interpolate(self.c2(self.p2(x)), (h, w))
        f3 = F.interpolate(self.c3(self.p3(x)), (h, w))
        return self.fuse(torch.cat([f1, f2, f3], dim=1))

class TinyBSCNet(nn.Module):
    def __init__(self, num_classes=5):
        super().__init__()
        self.enc1 = ConvBlock(3,32)
        self.enc2 = ConvBlock(32,64)
        self.enc3 = ConvBlock(64,128)

        self.pool = nn.MaxPool2d(2,2)

        self.bottleneck = ConvBlock(128,256)
        self.elppm = SimpleELPPM(256,128)

        self.up3 = nn.ConvTranspose2d(128,128,2,2)
        self.dec3 = ConvBlock(128+128,128)

        self.up2 = nn.ConvTranspose2d(128,64,2,2)
        self.dec2 = ConvBlock(64+64,64)

        self.up1 = nn.ConvTranspose2d(64,32,2,2)
        self.dec1 = ConvBlock(32+32,32)

        self.bfm = SimpleBFM(32,32)
        self.final = nn.Conv2d(32, 5, 1)

    def forward(self, x):
        e1 = self.enc1(x); p1 = self.pool(e1)
        e2 = self.enc2(p1); p2 = self.pool(e2)
        e3 = self.enc3(p2); p3 = self.pool(e3)

        b = self.bottleneck(p3)
        b = self.elppm(b)

        u3 = self.up3(b)
        d3 = self.dec3(torch.cat([u3, e3], dim=1))

        u2 = self.up2(d3)
        d2 = self.dec2(torch.cat([u2, e2], dim=1))

        u1 = self.up1(d2)
        d1 = self.dec1(torch.cat([u1, e1], dim=1))

        d1 = self.bfm(d1)
        return self.final(d1)

