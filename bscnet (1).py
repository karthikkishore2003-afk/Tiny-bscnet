    pixel_total = 0
    iou_list = []

    with torch.no_grad():
        for imgs, masks in val_loader:
            imgs, masks = imgs.cuda(), masks.cuda()

            pred = model(imgs).argmax(1)

            pixel_correct += (pred == masks).sum().item()
            pixel_total += masks.numel()

            for cls in range(5):
                pred_c = (pred == cls)
                mask_c = (masks == cls)
                inter = (pred_c & mask_c).sum().item()
                union = (pred_c | mask_c).sum().item()
                if union > 0:
                    iou_list.append(inter / union)

    acc = pixel_correct / pixel_total
    miou = np.mean(iou_list)

    return acc, miou

