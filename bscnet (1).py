def compute_metrics(loader, model, num_classes=5, device="cuda"):
    model.eval()
    pixel_correct = 0
    pixel_total = 0
    # accumulate IoU components per class
    intersections = np.zeros(num_classes, dtype=np.float64)
    unions = np.zeros(num_classes, dtype=np.float64)
    with torch.no_grad():
        for imgs, masks in loader:
            imgs = imgs.to(device)
            masks = masks.to(device)

            logits = model(imgs)
            preds = logits.argmax(dim=1)

            pixel_correct += (preds == masks).sum().item()
            pixel_total += masks.numel()

            for cls in range(num_classes):
                pred_c = (preds == cls)
                mask_c = (masks == cls)
                inter = (pred_c & mask_c).sum().item()
                union = (pred_c | mask_c).sum().item()
                intersections[cls] += inter
                unions[cls] += union

    pixel_acc = pixel_correct / pixel_total
    # per-class IoU (avoid divide by zero)
    ious = []
    for cls in range(num_classes):
        if unions[cls] > 0:
            ious.append(intersections[cls] / unions[cls])
        else:
            ious.append(float("nan"))  # class not present in dataset
    mean_iou = np.nanmean(ious)
    return pixel_acc, ious, mean_iou

