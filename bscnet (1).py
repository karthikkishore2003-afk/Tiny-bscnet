import torch
import torch.nn as nn
import torch.optim as optim
from torch.amp import autocast, GradScaler   # new API
import time
import json

device = "cuda"
EPOCHS = 20
LR = 3e-4

criterion = nn.CrossEntropyLoss()
optimizer = optim.AdamW(model.parameters(), lr=LR, weight_decay=1e-5)
