def draw_and_count(idx):
    pred_path = f"seg_project/predictions/{idx:04d}_pred.png"
    overlay_path = f"seg_project/overlays/{idx:04d}_overlay.png"

    if not (os.path.exists(pred_path) and os.path.exists(overlay_path)):
        return None

    pred = np.array(Image.open(pred_path))
    img = np.array(Image.open(overlay_path))

    boxes_info = []
    counts = Counter()

    # iterate over classes 1–4 (ignore road=0)
    for cls in [1,2,3,4]:
        mask = (pred == cls).astype("uint8") * 255

        # connected components
        n, labels, stats, _ = cv2.connectedComponentsWithStats(mask)

        for i in range(1, n):
            x, y, w, h, area = stats[i]

            # tiny noise filter
            if area < 20:
                continue

            counts[cls] += 1
            boxes_info.append((cls, x, y, x+w, y+h, int(area)))

            color = box_colors.get(cls, (255,255,255))
            img_bgr = cv2.cvtColor(img, cv2.COLOR_RGB2BGR)
            cv2.rectangle(img_bgr, (x,y), (x+w,y+h), color, 2)
            cv2.putText(img_bgr, f"{cls}:{area}", (x, y-5),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.45, color, 1)
            img = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

    # save boxed image
    out_path = f"seg_project/boxed/{idx:04d}_boxed.png"
    Image.fromarray(img).save(out_path)

    return {"idx": idx, "counts": dict(counts), "boxes": boxes_info}


